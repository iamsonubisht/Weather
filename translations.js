let currentLanguage = 'en';

function applyTranslations() {
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.textContent.trim();
    if (translations[currentLanguage][key]) {
      element.textContent = translations[currentLanguage][key];
    }
  });

  document.querySelectorAll('[data-translate-aria]').forEach(element => {
    const key = element.getAttribute('aria-label');
    if (translations[currentLanguage][key]) {
      element.setAttribute('aria-label', translations[currentLanguage][key]);
    }
  });

  document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
    const key = element.getAttribute('placeholder');
    if (translations[currentLanguage][key]) {
      element.setAttribute('placeholder', translations[currentLanguage][key]);
    }
  });

  document.title = translations[currentLanguage]['Weather-Blast'];
}

document.getElementById('language-switcher').addEventListener('change', (e) => {
  currentLanguage = e.target.value;
  localStorage.setItem('language', currentLanguage);
  applyTranslations();
});

document.addEventListener('DOMContentLoaded', () => {
  const savedLanguage = localStorage.getItem('language') || 'en';
  currentLanguage = savedLanguage;
  document.getElementById('language-switcher').value = currentLanguage;
  applyTranslations();
});